function productImageUrl(imageSeed) {
    return `https://picsum.photos/seed/${imageSeed}/800/800`;
}

function brandLogoUrl(brandName) {
    const slug = String(brandName || 'brand')
        .toLowerCase()
        .replace(/[^a-z0-9]+/g, '-')
        .slice(0, 40);
    return `https://picsum.photos/seed/brand-${slug}/200/200`;
}

function toProductDocument(raw, userId) {
    const img = productImageUrl(raw.imageSeed);
    const brandName = raw.brand?.name || 'Generic';
    const logo = brandLogoUrl(brandName);
    return {
        seedKey: raw.seedKey,
        name: raw.name,
        description: raw.description,
        category: raw.category,
        price: raw.price,
        cuttedPrice: raw.cuttedPrice,
        stock: raw.stock,
        ratings: raw.ratings,
        numOfReviews: raw.numOfReviews,
        warranty: 1,
        highlights: raw.highlights || [],
        specifications: raw.specifications || [
            { title: 'Warranty', description: '1 year manufacturer' },
            { title: 'Ships from', description: 'Pakistan warehouse' },
        ],
        images: [{ public_id: `seed-${raw.imageSeed}`, url: img }],
        brand: {
            name: brandName,
            logo: { public_id: `seed-brand-${raw.seedKey}`, url: logo },
        },
        user: userId,
        seo: {
            pageTitle: `${raw.name} | izzmarket`,
            metaDescription: raw.description.slice(0, 155),
            keywords: `${raw.category}, ${brandName}, buy online pakistan, izzmarket`,
            canonicalPath: '', // filled after insert
            robots: 'index, follow',
        },
    };
}

